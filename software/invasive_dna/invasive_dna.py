# âœ… Create Vision API client
client = vision.ImageAnnotatorClient()

# ğŸ” Process each image
for i, image_path in enumerate(sample_images, start=1):
    print(f"\nğŸ“¸ Sample {i}: {image_path}")

    with open(image_path, "rb") as img_file:
        content = img_file.read()

    image = vision.Image(content=content)
    response = client.text_detection(image=image)
    texts = response.text_annotations

    if not texts:
        print("âš ï¸ No text detected.")
        continue

    # ğŸ§¹ Clean the extracted DNA (keep only CGAT)
    extracted_raw = texts[0].description.upper()
    extracted_dna = ''.join(c for c in extracted_raw if c in "CGAT")

    

    # ğŸ§  Match against known DNA
    best_score = 0.0
    best_match = None

    for j, ref_dna in enumerate(invasive_carps):
        score = 1 - Levenshtein.distance(extracted_dna, ref_dna) / max(len(ref_dna), 1)
        if score > best_score:
            best_score = score
            best_match = species_names[j]

    # âœ… Output result
    if best_score >= 0.90:
        print(f"âœ… INVASIVE! Match: {best_match} ({best_score:.2%})")
